// C++ program to illustrate
// isspace() function

#include <iostream>
using namespace std;

int main()
{
	// taking input
	char ch = ' ';

	// checking is it space?
	if (isspace(ch))
		cout << "\nEntered character is space";
	else
		cout << "\nEntered character is not space";

	return 0;
}

